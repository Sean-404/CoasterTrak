/**
 * Returns a stable continent id for achievement logic, or null if unknown.
 */
export function continentIdForCountryLabel(country: string | null | undefined): ContinentId | null {
  const raw = (country ?? "").trim();
  if (!raw) return null;
  let key = raw.toLowerCase();
  key = ALIASES[key] ?? key;
  return NAME_TO_CONTINENT.get(key) ?? null;
}
