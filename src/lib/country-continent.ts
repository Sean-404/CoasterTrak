const NAME_TO_CONTINENT: Map<string, ContinentId> = (() => {
  const m = new Map<string, ContinentId>();
  for (const [cont, names] of Object.entries(BY_CONTINENT) as [ContinentId, readonly string[]][]) {
    for (const n of names) {
      m.set(n.trim().toLowerCase(), cont);
    }
  }
  return m;
})();

